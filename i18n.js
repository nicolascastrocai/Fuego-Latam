let CURRENT_LANG = localStorage.getItem('fuego_lang') || 'es';

function applyI18n(lang) {
  CURRENT_LANG = lang;
  localStorage.setItem('fuego_lang', lang);
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i]').forEach(el => {
    const key = el.getAttribute('data-i');
    const txt = (I18N[lang] && I18N[lang][key]) || (I18N.es[key] || '');
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = txt.replace(/<[^>]+>/g, '');
    } else {
      el.innerHTML = txt;
    }
  });
  document.querySelectorAll('.lang-toggle button').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === lang);
  });
}

window.addEventListener('DOMContentLoaded', () => {
  applyI18n(CURRENT_LANG);
  document.querySelectorAll('.lang-toggle button').forEach(b => {
    b.addEventListener('click', () => applyI18n(b.dataset.lang));
  });
});
